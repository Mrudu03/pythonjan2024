'''
Relational operation assiignment for all data types
'''

#numeric Data types
num1 = 259
num2 = 140
print(f"{num1 == num2 =}")   
print(f"{num1 >  num2 =}")
print(f"{num1 >= num2 =}")
print(f"{num1 <  num2 =}")
print(f"{num1 <= num2 =}")
print(f"{num1 != num2 =}")

#float data type
float1 = 3.14
float2 = 2.5
print(f"{float1 == float2 =}")   # Output: False
print(f"{float1 >  float2 =}")   # Output: True
print(f"{float1 >= float2 =}")   # Output: True
print(f"{float1 <  float2 =}")   # Output: False
print(f"{float1 <= float2 =}")   # Output: False
print(f"{float1 != float2 =}")   # Output: True

#boolean data type
bool1 = True
bool2 = False
print(f"{bool1 == bool2 =}")   # Output: False
print(f"{bool1 >  bool2 =}")   # Output: True
print(f"{bool1 >= bool2 =}")   # Output: True
print(f"{bool1 <  bool2 =}")   # Output: False
print(f"{bool1 <= bool2 =}")   # Output: False
print(f"{bool1 != bool2 =}")   # Output: True

#list data type
list1 = [1, 2, 3]
list2 = [1, 2, 3, 4]
print(f"{list1 == list2 =}")   # Output: False
print(f"{list1 >  list2 =}")   # Output: False
print(f"{list1 >= list2 =}")   # Output: False
print(f"{list1 <  list2 =}")   # Output: True
print(f"{list1 <= list2 =}")   # Output: True
print(f"{list1 != list2 =}")   # Output: True